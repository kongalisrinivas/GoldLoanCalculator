package com.goldloancalculator;

public class SingleItem {

    String Itemname, apraisalvalue,
     bankamount,
     grossweight,
     neight;

    public SingleItem() {

    }

    public String getItemname() {
        return Itemname;
    }

    public void setItemname(String itemname) {
        Itemname = itemname;
    }

    public String getApraisalvalue() {
        return apraisalvalue;
    }

    public void setApraisalvalue(String apraisalvalue) {
        this.apraisalvalue = apraisalvalue;
    }

    public String getBankamount() {
        return bankamount;
    }

    public void setBankamount(String bankamount) {
        this.bankamount = bankamount;
    }

    public String getGrossweight() {
        return grossweight;
    }

    public void setGrossweight(String grossweight) {
        this.grossweight = grossweight;
    }

    public String getNeight() {
        return neight;
    }

    public void setNeight(String neight) {
        this.neight = neight;
    }


}
